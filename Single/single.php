						<h2>Preparation</h2>
						<ol>
							<li>Make the dough: In a large bowl, whisk together the warm milk, sugar, and melted butter. The mixture should be just warm, registering between 100-110˚F (37-43˚C). If any warmer, allow to cool slightly.</li>
							<li>Sprinkle the yeast evenly over the milk mixture, stir, and let sit in a warm place for about 10 minutes until the yeast has bloomed.</li>
							<li>Add 4 cups (500 g) of flour to the milk mixture and stir with a wooden spoon until just combined.</li>
							<li>Cover the bowl with a kitchen towel or plastic wrap and let rise in a warm place for 1 hour, until nearly doubled in size.</li>
							<li>Generously butter 2 9-inch (23 cm) round baking pans and set aside.</li>
							<li>Make the filling: In a medium bowl, combine the brown sugar, butter, and cinnamon. Mix well, then set aside.</li>
							<li>Remove the plastic wrap from the dough and add the remaining cup of flour, baking powder, and salt. Stir well, then turn out onto a clean surface. Leave a bit of flour nearby to use as needed, but try not to incorporate too much.</li>
							<li>Knead the dough for at least 10 minutes, adding more flour as necessary, until the dough just loses its stickiness and does not stick to the surface or your hands. The dough should be very smooth and spring back when poked.</li>
							<li>Roll the dough out into a large rectangle, about ½-inch (1 cm) thick. Fix the corners with a bench scraper or a spatula to make sure they are sharp and even.</li>
							<li>Spread the filling evenly over the dough.</li>
							<li>Starting from one short end, roll up the dough into a log and pinch the seam closed. Place seam-side down. Trim any uneven ends.</li>
							<li>Using unflavored dental floss, cut the log into evenly pieces, about 1½ inches (8cm) thick. Place the cinnamon rolls in the prepared pans, 1 in the center and about 5 around the sides. Cover with a towel and let rise in a warm place for 35-45 minutes, until expanded by about half of their original volume. If you’re saving the cinnamon rolls for later, cover with plastic wrap and freeze for up 3 months.</li>
							<li>Preheat the oven to 350˚F (180˚C).</li>
							<li>Bake the cinnamon rolls for 25-30 minutes, until golden brown.</li>
							<li>While the cinnamon rolls are baking, make the frosting: In a medium bowl, whisk together the cream cheese, melted butter, milk, and vanilla extract until smooth. Gradually add the powdered sugar and whisk until homogenous and runny.</li>
							<li>When the cinnamon rolls are finished, let cool for about 10 minutes.</li>
							<li>Drizzle the frosting over the cinnamon rolls, using the back of a spoon to spread if desired.</li>
							<li>Enjoy!</li>
						</ol>
